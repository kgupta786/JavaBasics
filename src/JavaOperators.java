public class JavaOperators {
    public void process(){
        //Arithmetic operators
        System.out.println("Sum is "+ (2+3));
        System.out.println("Difference is "+ (2-3));
        System.out.println("Product is "+ (2*3));
        System.out.println("Quotient is "+ (2/3));

        // Logical Operators
        System.out.println("Logical And is "+ (true && false));
        System.out.println("Logical OR is "+ (true || false));
        System.out.println("Logical NOT is "+ (!true));

    }
}

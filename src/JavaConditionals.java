public class JavaConditionals {
    /**
     * Conditionals in Java are nothing but if-else conditions based upon which we try to print something
     *
     */

    public void process(){
        int num = 12;
        if(num>0){
            System.out.println("number is positive");
        } else if(num < 0){
            System.out.println("Number is negative");
        } else{
            System.out.println("Number is zero");
        }
    }
}
